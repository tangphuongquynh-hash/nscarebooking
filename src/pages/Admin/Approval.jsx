function getStatusBadge(status) {
  const badgeStyle = {
    minWidth: '120px',
    textAlign: 'center',
    whiteSpace: 'nowrap'
  };
  
  switch (status) {
    case "pending":
      return <ThemedBadge variant="warning" size="sm" style={badgeStyle}>‚è≥ ƒêang ch·ªù</ThemedBadge>;
    case "confirmed":
      return <ThemedBadge variant="primary" size="sm" style={badgeStyle}>‚úÖ X√°c nh·∫≠n</ThemedBadge>;
    case "completed":
      return <ThemedBadge variant="success" size="sm" style={badgeStyle}>üéâ Ho√†n th√†nh</ThemedBadge>;
    case "cancelled":
      return <ThemedBadge variant="error" size="sm" style={badgeStyle}>‚ùå ƒê√£ hu·ª∑</ThemedBadge>;
    default:
      return null;
  }
}

